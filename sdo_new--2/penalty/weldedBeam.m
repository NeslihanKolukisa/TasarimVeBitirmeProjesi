function p = weldedBeam(x)

    t1 = 6000 / (sqrt(2) * x(1) * x(2)); 
    t2 = (6000 * (14 + 0.5 * x(2)) * sqrt(0.25 * (x(2)^2 + (x(1) + x(3))^2))) / (2 * (0.707 * x(1) * x(2) * ((x(2)^2 / 12) + 0.25 * (x(1) + x(3))^2)));
    tZ = sqrt(t1^2 + t2^2 + ((x(2) * t1 * t2) / sqrt(0.25 * (x(2)^2 + (x(1) + x(3))^2))));
    sigmaZ = 504000 / (x(3)^2 * x(4));
    deltaZ = 65856000 / (30 * 1E6 * x(4) * x(3)^3);
    pcZ = 64746.022 * (1 - 0.0282346 * x(3)) * x(3) * x(4)^3;
    c1 = tZ - 13600;
    c2 = sigmaZ - 30000;
    c3 = deltaZ - 0.25;
    c4 = x(1) - x(4);
    c5 = 6000 - pcZ;
    c6 = 0.125 - x(1);
    c7 = 1.10471 * x(1)^2 + 0.04811 * x(3) * x(4) * (14 + x(2)) - 5;
    p = get_penalty([c1, c2, c3, c4, c5, c6, c7]);
    
end